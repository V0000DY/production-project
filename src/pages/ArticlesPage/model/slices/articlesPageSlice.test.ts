describe("articleDetailsCommentsSlice.test", () => {
  const initialState: ArticlesPageSchema = {
    ids: [],
    entities: {},
    isLoading: false,
    error: undefined,
    view: ArticleView.SMALL,
  };

  test("should return initial state", () => {
    expect(articlesPageReducer(undefined, { type: "" })).toEqual(initialState);
  });

  test("fetchArticlesList.pending: setup isLoading true and error undefined", () => {
    const state = { ...initialState, error: "error" };
    const next = articlesPageReducer(state, fetchArticlesList.pending(""));
    expect(next.isLoading).toBe(true);
    expect(next.error).toBeUndefined();
  });

  test("fetchArticlesList.fulfilled: save articles and setup isLoading false", () => {
    const state = { ...initialState, isLoading: true };
    const articles = [article1, article2];
    const next = articlesPageReducer(
      state,
      fetchArticlesList.fulfilled(articles, "")
    );
    expect(next.isLoading).toBe(false);
    expect(next.ids).toEqual(["1", "2"]);
    expect(next.entities["1"]).toEqual(article1);
    expect(next.entities["2"]).toEqual(article2);
  });

  test("fetchArticlesList.rejected: setup isLoading false and error", () => {
    const state = { ...initialState, isLoading: true };
    const error = "Network error";
    const next = articlesPageReducer(
      state,
      fetchArticlesList.rejected(new Error(error), "", undefined, error)
    );
    expect(next.isLoading).toBe(false);
    expect(next.error).toBe(error);
  });
});
